    private void filtroUserKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_filtroUserKeyReleased
        DefaultTableModel model = (DefaultTableModel) jTableUsuarios.getModel();
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(model);
        jTableUsuarios.setRowSorter(sorter);

        String textoFiltro = filtroUser.getText().trim();

        if (textoFiltro.length() == 0) {
            sorter.setRowFilter(null);
        } else {
            sorter.setRowFilter(RowFilter.regexFilter("(?i)" + textoFiltro, 0, 1, 3));
        }
    }//GEN-LAST:event_filtroUserKeyReleased

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        String nomeUsuario = nome.getText().trim();
        String emailUsuario = email.getText().trim();
        String telefoneUsuario = telefone.getText().trim();
        String senhaUsuario = senha.getText();

        if (nomeUsuario.isEmpty() || emailUsuario.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nome e E-mail são obrigatórios.", "Erro de Validação", JOptionPane.ERROR_MESSAGE);
            return;
        }

        SistemaBiblioteca sistema = telaPrincipal.getSistema();
    
        if(sistema.buscarUsuario(nomeUsuario) != null) {
            JOptionPane.showMessageDialog(this, "Já existe um usuário com este nome.", "Erro de Validação", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String perfilSelecionado = (String) tipoUser.getSelectedItem();
        Usuario novoUsuario;

        if (perfilSelecionado.equals("Administrador")) {
            novoUsuario = new Administrador(nomeUsuario, emailUsuario, telefoneUsuario, senhaUsuario);
        } else {
            novoUsuario = new Usuario(nomeUsuario, emailUsuario, telefoneUsuario, senhaUsuario, PerfilUsuario.USUARIO_COMUM);
        }

        sistema.adicionarUsuario(novoUsuario);
    
        JOptionPane.showMessageDialog(this, "Usuário salvo com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
    
        limparCampos();
        atualizarTabelaUsuarios();
    }//GEN-LAST:event_btnSalvarActionPerformed

