    public void atualizarTabelaUsuarios() {
        DefaultTableModel model = (DefaultTableModel) jTableUsuarios.getModel();
        model.setRowCount(0);

        List<Usuario> usuarios = telaPrincipal.getSistema().getUsuarios();

        for (Usuario usuario : usuarios) {
            Object[] rowData = {
                usuario.getNome(),
                usuario.getEmail(),
                usuario.getTelefone(),
                usuario.getPerfil().toString()
            };
            model.addRow(rowData);
        }
    }

    private void limparCampos() {
        nome.setText("");
        email.setText("");
        telefone.setText("");
        tipoUser.setSelectedIndex(0);
    }


